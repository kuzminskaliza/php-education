$cart = [
    ['name' => 'Apple', 'price' => 1.2, 'quantity' => 10],
    ['name' => 'Banana', 'price' => 0.8, 'quantity' => 5],
    ['name' => 'Orange', 'price' => 1.5, 'quantity' => 7],
    ['name' => 'Milk', 'price' => 3.5, 'quantity' => 2],
    ['name' => 'Bred', 'price' => 0.9, 'quantity' => 2],
    ['name' => 'Beer', 'price' => 1.6, 'quantity' => 4],
];
$total = 0;
foreach ($cart as $item){
    echo $item['name'] . ': $' ;
    echo $item['price'] * $item['quantity'] . PHP_EOL;
    $total += $item['price'] * $item['quantity'];
}
echo 'Total' . ': $' .$total . PHP_EOL;

