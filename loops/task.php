// Кожен товар у кошику представлений асоціативним масивом з ключами
// name (назва товару), price (ціна за одиницю) і quantity (кількість).
// Скрипт повинен вивести список товарів з їх загальною вартістю і загальну вартість усіх товарів у кошику.
