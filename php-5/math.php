<?php
//повертає значення PI
echo(pi());

//для пошуку наймешного або найбільшого числа
echo(min(0, 150, 30, 20, -8, -200));
echo(max(0, 150, 30, 20, -8, -200));

//abs() виводить абсолютне додатнє число
echo(abs(-6.7));

//sqrt() виводить квадратний корінь з числа
echo(sqrt(64));

//round() округлє число з комою до найближчого цілого
echo(round(0.60));
echo(round(0.49));

//rand() генерує рандомне число
echo(rand());
echo(rand(10, 100));



